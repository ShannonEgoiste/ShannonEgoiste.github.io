var listCountry = [];
function getListCountry(settings){

    Object.keys(ISO_CODE_AND_NAME_LIST).forEach(x=>{
        if(settings.includes(ISO_CODE_AND_NAME_LIST[x].subregion)){
            var country = {
                n:ISO_CODE_AND_NAME_LIST[x].name,
                i:x
            };
            listCountry.push(country);
        }
    });
    return listCountry;
}

function isoFromName(name){
	var isoCode = -1;
	var maxSimilarity = 1000;
	listCountry.forEach((x)=>{
		var similarity = x.n.indexOf(name);
		if(similarity > -1){
			if(similarity < maxSimilarity){
				isoCode = x.i;
				maxSimilarity = similarity;
			}

		}
	});
	return isoCode;
}


